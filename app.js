const locale = (document.documentElement.lang || "de").slice(0, 2).toLowerCase();
const text = TEXT_BY_LOCALE[locale] || TEXT_BY_LOCALE.de;

const applyTranslations = () => {
  if (text.pageTitle) {
    document.title = text.pageTitle;
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    const value = text[key];

    if (typeof value === "string") {
      element.textContent = value;
    }
  });
};

applyTranslations();

const RSVP_EMAIL_ADDRESS = "boda@crelur.com";
const RSVP_MAIL_KEYS_BY_INTENT = {
  coming: { subjectKey: "mailSubjectComing", bodyKey: "mailBodyComing" },
  decline: { subjectKey: "mailSubjectDecline", bodyKey: "mailBodyDecline" },
  maybe: { subjectKey: "mailSubjectMaybe", bodyKey: "mailBodyMaybe" },
};

const openRsvpEmail = (intent) => {
  const keys = RSVP_MAIL_KEYS_BY_INTENT[intent];

  if (!keys) {
    return;
  }

  const subject = text[keys.subjectKey] || "";
  const body = text[keys.bodyKey] || "";
  const mailtoUrl = `mailto:${RSVP_EMAIL_ADDRESS}?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(body)}`;
  window.location.href = mailtoUrl;
};

const bindRsvpButtons = () => {
  document.querySelectorAll("[data-mail-intent]").forEach((button) => {
    button.addEventListener("click", () => {
      openRsvpEmail(button.getAttribute("data-mail-intent"));
    });
  });
};

bindRsvpButtons();

const applyVersionedImage = (imageElement, options) => {
  if (!imageElement) {
    return;
  }

  if (options && Object.prototype.hasOwnProperty.call(options, "alt")) {
    imageElement.alt = options.alt;
  }

  const imageUrl = new URL(imageElement.getAttribute("src"), window.location.href);
  imageUrl.searchParams.set("v", window.__assetVersion || Date.now().toString());
  imageElement.src = imageUrl.toString();

  if (options && options.fallbackPath) {
    imageElement.addEventListener("error", () => {
      if (imageElement.dataset.fallbackLoaded === "1") {
        return;
      }

      imageElement.dataset.fallbackLoaded = "1";
      imageElement.src = `${options.fallbackPath}?v=${window.__assetVersion || Date.now().toString()}`;
    });
  }
};

